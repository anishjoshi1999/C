#include <stdio.h>
int main()
{
    // a length of seven
    float n = 12.3456;
    printf("%1.4f\n", n);
    printf("%2.4f\n", n);
    printf("%3.4f\n", n);
    printf("%4.4f\n", n);
    printf("%5.4f\n", n);
    printf("%6.4f\n", n);
    printf("%7.4f\n", n);
    printf("%8.4f\n", n);
    printf("%08.1f\n", n);

    return 0;
}